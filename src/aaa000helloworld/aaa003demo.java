package aaa000helloworld;

public class aaa003demo {
    public static void main(String[] args) {
//        算数运算符
        int a = 10;
        char b = 'A';
        b = 'a';
        b = '0';
        System.out.print("字符加和：");
        System.out.println(a + b);
        //A为65，
        // a为97,
        // 0为48,
        System.out.println("有字符串，加号变连接号：" + a + b);
        System.out.println("有字符串，加号变连接号：" + b + "111111");
    }
}

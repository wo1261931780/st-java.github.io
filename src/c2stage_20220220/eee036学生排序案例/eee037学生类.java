package c2stage_20220220.eee036学生排序案例;

public class eee037学生类 {
    private String name;
    private int age;
    private int num;

    public eee037学生类(String name, int age, int num) {
        this.name = name;
        this.age = age;
        this.num = num;
    }

    public eee037学生类() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
